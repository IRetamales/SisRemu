    public void Load(){ //Funcion para realizar la carga inicial del formulario
        clsSalud salud = new clsSalud();
        clsPension pension = new clsPension();
        SimpleDateFormat dateFormat = new SimpleDateFormat("dd-MM-yyyy");
        
        this.setDefaultCloseOperation(WindowConstants.DISPOSE_ON_CLOSE);
        
        salud.CargaInstitucionSalud(cmbInstitucionSalud);
        pension.LlenaComboInstitucionPension(cmbSistemaNuevo,"0");
        pension.LlenaComboInstitucionPension(cmbSistemaAntiguo,"1");
        
        optFonasa.setSelected(true);
        optSistemaNuevo.setSelected(true);
        
        try{
            
            if (intAccion==1){
                btnModificar.setVisible(false);
            }else{
                btnGuardar.setVisible(false);

                clsFuncionario Fun = new clsFuncionario();

                Fun.setRut(strRut);
                Fun.CargarFuncionario();

                txtRut.setText(Fun.getRut());
                txtNombres.setText(Fun.getNombre());
                txtApellidoPaterno.setText(Fun.getApellidoPaterno());
                txtApellidoMaterno.setText(Fun.getApellidoMaterno());
                txtFechaNacimiento.setDate(dateFormat.parse(Fun.getFechaNacimiento()));
                //txtFechaNacimiento.setDate(6, new java.sql.Date(Fun.getFechaNacimiento()));
                //Date FechaDate = dateFormat.parse(Fun.getFechaNacimiento());
                //txtFechaNacimiento.setDate(FechaDate);
                txtDireccion.setText(Fun.getDireccion());
                txtTelefono.setText(Fun.getTelefono());
                txtCelular.setText(Fun.getCelular());
                txtMail.setText(Fun.getMail());
                
                if (Fun.getSalud()!=00){
                    cmbInstitucionSalud.setSelectedIndex(Fun.getSalud()-1);
                    optIsapre.setSelected(true);
                    cmbInstitucionSalud.setEnabled(true);
                }
                
                clsPension Pension = new clsPension();
                Pension.CargaInstitucionPension(Fun.getPension());
                if (Pension.getTipo()!="0"){
                    cmbSistemaNuevo.setSelectedIndex(Fun.getPension()-1);
                }else{
                    cmbSistemaAntiguo.setSelectedIndex(Fun.getPension()-1);
                    optSistemaAntiguo.setSelected(true);
                    cmbSistemaAntiguo.setEnabled(true);
                }
            
            }
        } catch (Exception e) {
            e.printStackTrace();     
        }    
    } 
           
