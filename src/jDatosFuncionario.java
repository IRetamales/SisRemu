    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        SimpleDateFormat dateFormat = new SimpleDateFormat("yyyyMMdd");
        clsFuncionario Fun = new clsFuncionario();
        
        Fun.setRut(txtRut.getText());
        Fun.setNombre(txtNombres.getText());
        Fun.setApellidoPaterno(txtApellidoPaterno.getText());
        Fun.setApellidoMaterno(txtApellidoMaterno.getText());
        //Fun.setFechaNacimiento(dateFormat.parse(txtFechaNacimiento.getDate()));
        Fun.setFechaNacimiento(String.format("%tY%tm%td",txtFechaNacimiento.getDate(),txtFechaNacimiento.getDate(),txtFechaNacimiento.getDate()));
        Fun.setDireccion(txtDireccion.getText());
        Fun.setTelefono(txtTelefono.getText());
        Fun.setCelular(txtCelular.getText());
        Fun.setMail(txtMail.getText());
        Fun.setSalud(cmbInstitucionSalud.getItemAt(cmbInstitucionSalud.getSelectedIndex()).getIdInstitucion());
        if (cmbSistemaNuevo.isEnabled()){
            Fun.setPension(cmbSistemaNuevo.getItemAt(cmbSistemaNuevo.getSelectedIndex()).getIdInstitucion());
        }else{
            Fun.setPension(cmbSistemaAntiguo.getItemAt(cmbSistemaAntiguo.getSelectedIndex()).getIdInstitucion());
        }
        if (Fun.GuardarFuncionario()){
            JOptionPane.showMessageDialog(this, "Funcionario agregado exitosamente!", "Datos guardados!", JOptionPane.INFORMATION_MESSAGE);
        }else{
            JOptionPane.showMessageDialog(this, "Ha ocurrido un error!", "Error en guardar datos!", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

